// variable declaration
var tomatoPrice = 25;

// variable value update
tomatoPrice = 30;

// add something to the existing value
var price1 = 35;
var price2 = 20;

price1 = price1 + 5;
console.log(price1);

// addition to the same value shorthand
// += means adding some value to previous value
price2 += 10;
console.log(price2);

var age = 15;
// adding 1 to the existing value
age++;
console.log(age);

var love = 100;
// deducting 1 from existing value
love--;

